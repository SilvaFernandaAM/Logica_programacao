var dia = "Quarta";

switch (dia) {
    case "Segunda":
        console.log("Dia de começar a semana!");
    break;

    case "Terça":
        console.log("Hora de aprender coisas novas!");
    break;

    case "Quarta":
        console.log("Metade da semana. Continue firme!");
    break;

    case "Quinta":
        console.log("Quase lá! Um pouco mais de esforço!");
    break;

    case "Sexta":
        console.log("Dia de comemorar, o fim de semana chegou!");
    break;

    default:
        console.log("Fim de semana chegou")
    break;
}